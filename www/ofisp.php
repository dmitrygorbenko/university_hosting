<center><font size="+2">Нормы пользования Сетью</font></center>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Сеть Интернет представляет собой глобальное объединение компьютерных
сетей и информационных ресурсов, принадлежащих множеству различных людей и
организаций. Это объединение является децентрализованным, и единого
общеобязательного свода правил (законов) пользования сетью Интернет не
установлено. Существуют, однако, общепринятые нормы работы в сети
Интернет, направленные на то, чтобы деятельность каждого пользователя сети
не мешала работе других пользователей. Фундаментальное положение этих норм<br>
таково: правила использования любых ресурсов сети Интернет определяют
владельцы этих ресурсов и только они (здесь и далее словом "ресурс"
обозначается любая совокупность программных и аппаратных средств,
составляющих в том или ином смысле единое целое. Ресурсом сети Интернет
могут считаться, например, почтовый ящик, персональный компьютер,
виртуальный или физический сервер, локальная вычислительная сеть, канал
связи и т.д.)<br>
&nbsp;&nbsp;&nbsp;&nbsp;Настоящий документ представляет собой одно из возможных формальных<br>
описаний общепринятых норм сетевого взаимодействия, считающихся в
большинстве сетей (как входящих в сеть Интернет непосредственно, так и
доступных из сети Интернет тем или иным опосредованным образом)
обязательными к исполнению всеми пользователями. Такие или аналогичные
нормы применяются в отношении всех доступных сетевых ресурсов, когда
заранее не известны правила, установленные владельцами этих ресурсов
самостоятельно.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Как показывает практика, большинство пользователей сети Интернет ожидает
от других пользователей исполнения общепринятых сетевых норм, т.к. их
нарушение влечет серьезные затруднения работы в Сети, как технические, так
и обусловленные человеческим фактором.
&nbsp;&nbsp;&nbsp;&nbsp;При создании документа не ставилось целей формулировать
универсальные правила работы в Сети, дублировать положения
законодательства тех или иных государств и т.п. Документ
охватывает исключительно внутрисетевые нормативы, сложившиеся в
международном сетевом сообществе как проявление самосохранения
сети Интернет.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Авторы документа надеются, что данная формализация общепринятых норм
окажется полезной как администраторам сетей при разработке правил
доступа для пользователей, так и конечным пользователям Сети для избежания
конфликтных ситуаций в повседневной работе. Кроме того, данный документ
поможет определить, какого поведения разумно ожидать пользователю от
других участников сетевого взаимодействия и в каких случаях можно считать
себя пострадавшим от недопустимых действий.<br>
<br>
<br>
<font size="+1">1. Ограничения на информационный шум (спам)</font><br>
<br>
Развитие Сети привело к тому, что одной из основных проблем
пользователей стал избыток информации. Поэтому сетевое сообщество
выработало специальные правила, направленные на ограждение
пользователя от ненужной/незапрошенной информации (спама). В
частности, являются недопустимыми:<br>
<br>
<br>
1.1. Массовая рассылка сообщений посредством электронной почты и других
средств персонального обмена информацией (включая службы немедленной
доставки сообщений, такие как SMS, IRC и т.п.), иначе как по явно и
недвусмысленно выраженной инициативе получателей.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Открытая публикация адреса электронной почты или другой системы
персонального обмена информацией не может служить основанием для включения
адреса в какой-либо список для массовой рассылки сообщений. Включение
адреса, полученного любым путем (через веб-форму, через подписного робота
и т.п.), в список адресов, по которому проводится какая-либо рассылка,
допускается только при условии наличия надлежащей технической процедуры
подтверждения подписки, гарантирующей, что адрес не попадет в список
иначе, как по воле владельца адреса. Процедура подтверждения подписки
должна исключать возможность попадания адреса в список адресатов
какой-либо рассылки (единичной или регулярной) по инициативе третьих лиц
(т.е. лиц, не являющихся владельцами данного адреса).<br>
&nbsp;&nbsp;&nbsp;&nbsp;Обязательно наличие возможности для любого подписчика немедленно
покинуть список рассылки без каких-либо затруднений при возникновении у
него такого желания. При этом наличие возможности покинуть список само по
себе не может служить оправданием внесения адресов в список не по воле
владельцев адресов.<br>
<br>
1.2. Отправка электронных писем и других сообщений, содержащих вложенные
файлы и/или имеющих значительный объем, без предварительно полученного
разрешения адресата.<br>
<br>
1.3. Рассылка (иначе как по прямой инициативе получателя)<br>
&nbsp;&nbsp;&nbsp;&nbsp; а) электронных писем и других сообщений (в том числе единичных)
рекламного, коммерческого или агитационного характера;<br>
&nbsp;&nbsp;&nbsp;&nbsp; б) писем и сообщений, содержащих грубые и оскорбительные выражения и
предложения.<br>
&nbsp;&nbsp;&nbsp;&nbsp; в) Рассылка сообщений, содержащих просьбу переслать данное сообщение
другим доступным пользователям (chain letters).<br>
&nbsp;&nbsp;&nbsp;&nbsp; г) Использование безличных ("ролевых") адресов иначе, как по их
прямому назначению, установленному владельцем адресов и/или стандартами.<br>
<br>
1.4. Размещение в любой электронной конференции сообщений, которые не
соответствуют тематике данной конференции (off-topic). Здесь и далее под
конференцией понимаются телеконференции (группы новостей) Usenet и другие
конференции, форумы и списки рассылки.<br>
<br>
1.5. Размещение в любой конференции сообщений рекламного, коммерческого
или агитационного характера, кроме случаев, когда такие сообщения явно
разрешены правилами данной конференции либо их размещение было согласовано
с владельцами или администраторами данной конференции предварительно.<br>
<br>
1.6. Размещение в любой конференции статьи, содержащей приложенные
файлы, кроме случаев, когда вложения явно разрешены правилами данной
конференции либо такое размещение было согласовано с владельцами или
администраторами конференции предварительно.<br>
<br>
1.7. Рассылка информации получателям, ранее в явном виде выразившим
нежелание получать эту информацию, информацию данной категории или
информацию от данного отправителя.<br>
<br>
1.8. Использование собственных или предоставленных информационных ресурсов
(почтовых ящиков, адресов электронной почты, страниц WWW и т.д.) в
качестве контактных координат при совершении любого из вышеописанных
действий, вне зависимости от того, из какой точки Сети были совершены эти
действия.<br>
<br>
1.9. Осуществление деятельности по техническому обеспечению рассылки
спама (spam support service), как то:<br>
&nbsp;&nbsp;&nbsp;&nbsp;- целенаправленное сканирование содержимого информационных ресурсов с
целью сбора адресов электронной почты и других служб доставки сообщений;<br>
&nbsp;&nbsp;&nbsp;&nbsp;- распространение программного обеспечения для рассылки спама;<br>
&nbsp;&nbsp;&nbsp;&nbsp;- создание, верификация, поддержание или распространение баз данных
адресов электронной почты или других служб доставки сообщений (за
исключением случая, когда владельцы всех адресов, включенных в такую базу
данных, в явном виде выразили свое согласие на включение адресов в данную
конкретную базу данных; открытая публикация адреса таковым согласием
считаться не может).<br>
<br>
<br>
<font size="+1">2. Запрет несанкционированного доступа и сетевых атак</font><br>
<br>
Не допускается осуществление попыток несанкционированного доступа к
ресурсам Сети, проведение сетевых атак и сетевого взлома и участие в них,
за исключением случаев, когда атака на сетевой ресурс проводится с явного
разрешения владельца или администратора этого ресурса. В том числе
запрещены:<br>
<br>
2.1. Действия, направленные на нарушение нормального функционирования
элементов Сети (компьютеров, другого оборудования или программного
обеспечения), не принадлежащих пользователю.<br>
<br>
2.2. Действия, направленные на получение несанкционированного доступа к
ресурсу Сети (компьютеру, другому оборудованию или информационному
ресурсу), последующее использование такого доступа, а также уничтожение
или модификация программного обеспечения или данных, не принадлежащих
пользователю, без согласования с владельцами этого программного
обеспечения или данных либо администраторами данного информационного
ресурса. Под несанкционированным доступом понимается любой доступ
способом, отличным от предполагавшегося владельцем ресурса.<br>
<br>
2.3. Передача компьютерам или оборудованию Сети бессмысленной или
бесполезной информации, создающей паразитную нагрузку на эти
компьютеры или оборудование, а также промежуточные участки сети, в
объемах, превышающих минимально необходимые для проверки связности
сетей и доступности отдельных ее элементов.<br>
<br>
2.4. Целенаправленные действия по сканированию узлов сетей с
целью выявления внутренней структуры сетей, списков открытых
портов и т.п., иначе как в пределах, минимально необходимых
для проведения штатных технических мероприятий, не ставящих
своей целью нарушение пунктов 2.1 и 2.2 настоящего документа.
<br>
