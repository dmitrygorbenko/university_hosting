<font size="+1">5. Настройка собственных ресурсов</font><br>
<br>
При работе в сети Интернет пользователь становится ее полноправным
участником, что создает потенциальную возможность для использования
сетевых ресурсов, принадлежащих пользователю, третьими лицами. В связи
с этим пользователь должен принять надлежащие меры по такой настройке
своих ресурсов, которая препятствовала бы недобросовестному
использованию этих ресурсов третьими лицами, а при обнаружении случаев
такого использования принимать оперативные меры по их прекращению.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Примерами потенциально проблемной настройки сетевых ресурсов являются:<br>
&nbsp;&nbsp;&nbsp;&nbsp;- открытые ретрансляторы электронной почты (open SMTP-relays);<br>
&nbsp;&nbsp;&nbsp;&nbsp;- общедоступные для неавторизованной публикации серверы новостей
(конференций, групп);<br>
&nbsp;&nbsp;&nbsp;&nbsp;- средства, позволяющие третьим лицам неавторизованно скрыть
источник соединения (открытые прокси-серверы и т.п.);<br>
&nbsp;&nbsp;&nbsp;&nbsp;- общедоступные широковещательные адреса локальных сетей, позволяющие
проводить с их помощью атаки типа smurf;<br>
&nbsp;&nbsp;&nbsp;&nbsp;- электронные списки рассылки с недостаточной надежностью механизма
подтверждения подписки или без возможности ее отмены;<br>
&nbsp;&nbsp;&nbsp;&nbsp;- www-сайты и другие подобные ресурсы, осуществляющие отправку
корреспонденции третьим лицам по анонимному или недостаточно
аутентифицированному запросу.<br>
