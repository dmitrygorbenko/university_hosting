<br>
<font size="+1">3. Соблюдение правил, установленных владельцами ресурсов</font><br>
<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Владелец любого информационного или технического ресурса Сети может
установить для этого ресурса собственные правила его использования.
&nbsp;&nbsp;&nbsp;&nbsp;Правила использования ресурсов либо ссылка на них публикуются
владельцами или администраторами этих ресурсов в точке подключения к таким
ресурсам и являются обязательными к исполнению всеми пользователями этих
ресурсов. Правила должны быть легко доступными, написанными с учетом
разного уровня подготовки пользователей.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Правила использования ресурса, установленные владельцем, не должны
нарушать права владельцев других ресурсов или приводить к злоупотреблениям
в отношении других ресурсов.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Пользователь обязан соблюдать правила использования ресурса либо
немедленно отказаться от его использования.<br>
&nbsp;&nbsp;&nbsp;&nbsp;В случае, если правила, установленные владельцем ресурса, противоречат
тем или иным пунктам настоящего документа, в отношении данного ресурса
применяются правила, установленные владельцем, если это не ведет к
нарушениям в отношении других ресурсов. В случае, если владельцем группы
ресурсов явно установлены правила только для части ресурсов, для остальных
применяются правила, сформулированные в данном документе.
<br>
<br>
<br>
<font size="+1">4. Недопустимость фальсификации</font><br>
<br>
Значительная часть ресурсов Сети не требует идентификации пользователя
и допускает анонимное использование. Однако в ряде случаев от
пользователя требуется предоставить информацию, идентифицирующую его и
используемые им средства доступа к Сети. При этом пользователь не должен:<br>
<br>
4.1. Использовать идентификационные данные (имена, адреса, телефоны
и т.п.) третьих лиц, кроме случаев, когда эти лица уполномочили
пользователя на такое использование.<br>
<br>
4.2. Фальсифицировать свой IP-адрес, а также адреса, используемые в
других сетевых протоколах, при передаче данных в Сеть.<br>
<br>
4.3. Использовать несуществующие обратные адреса при отправке
электронных писем и других сообщений.<br>
<br>
4.4. Небрежно относиться к конфиденциальности собственных
идентификационных реквизитов (в частности, паролей и прочих кодов
авторизованного доступа), что может привести к использованию тех
или иных ресурсов третьими лицами от имени данного пользователя
(с сокрытием, таким образом, истинного источника действий).<br>
