1. ОБЩИЕ ПОЛОЖЕНИЯ
<p align="justify">
1.1. Данные Правила Пользования Услугами Сервиса "ХОСТИНГ В ХАИ"
(далее, Правила) являются обязательными для каждого клиента.
Цель данных правил обеспечить функционирование всех
сервисов Хостинга на высоком уровне, защитить клиентов
Хостинга от любой противозаконной деятельности, а также
определить порядок взаимодействия Администраторов Сервиса "ХОСТИНГ В ХАИ"
(далее Администраторов) с клиентами и третьими лицами.
Несоблюдение Правил может явиться причиной блокировки аккаунта.
</p>
2. ЗАПРЕЩЕННЫЕ ДЕЙСТВИЯ
<p align="justify">
2.1. Передача, распространение, публикация или хранение любой
информации, противоречащей законам Украины, а также нормам
сетевого этикета (http://www.ofisp.org/, доступные <a target="_blank" href="ofisp.php">здесь</a>) запрещено. Примеры
запрещенной информации и ссылок: информация, способствующая
распространению наркотиков и оружия, попыткам
несанкционированного доступа к компьютерным системам,
пропагандирующая жестокость и насилие, расовую или
религиозную ненависть, схемы обмана, информация, защищаемая
авторским правом, порнография, crack материалы (информация
для взлома программного обеспечения, защищенного авторским
правом), базы email адресов, предназначенные для рассылки
спама (spam), программы, которые могут быть использованы
для рассылки спама.

<p align="justify">
2.2. Под спамом (spam) понимается любая массовая рассылка
рекламных сообщений в любом виде и с помощью любых
технологий лицам, не подписывавшимся на запрос такой
информации. Спамом также является осуществление массовых
рекламных действий на чужих ресурсах без согласия
владельцев, включая массовые рекламные сообщения, рассылка
рекламной информации, не соответствующей теме конференции.
Спамом не является адресная рассылка на email адреса
подписчиков сайта, добровольно подписавшихся на рассылку
(например, рассылка новостей сайта). Такая и подобные
рассылки разрешены.
</p>

<p align="justify">
2.3. Запрещено использование сетевых ресурсов Хостинга для
нелегальных действий. Нелегальные действия включают
следующие: рассылка спама, вирусов, рассылка информации
частного характера о персоне без ее согласия,
распространение клеветы на физическое или юридическое
лицо, проведения азартных игр без соответствующего
разрешения, действия, направленные на получение
несанкционированного доступа к ресурсам других клиентов
Хостинга или любым внешним ресурсам.
</p>

<p align="justify">
2.4. Запрещается использование в email неправильного или
несуществующего обратного адреса отправителя, trolling
(посылка некорректного сообщения в конференцию для
разжигания спора), mail-bombing (отправление в один
почтовый ящик множества одинаковых сообщений), подписка
кого-либо на лист рассылки без его согласия.
</p>

