</p>

<p align="justify">
4.1. Техническая поддержка.
<br>
Администраторы оказывают техническую поддержку клиентам,
однако, мы не можем предоставить консультацию по любой программе,
созданной не нами. В первую очередь клиент должен
обращаться к разработчику программного обеспечения или
скрипта, но мы всегда готовы оказать посильную помощь.
Настройка дополнительного программного обеспечения
стороннего разработчика может быть осуществлена на платной
основе. Что касается использования сервисов Хостинга, в
частности, Панели Управления, мы всегда готовы предоставить
консультацию. Вопросы, требующие детального рассмотрения,
решаются по электронной почте. Для получения
квалифицированного ответа необходимо подробно описать
проблему (указать логин, имя домена, пути,
последовательность Ваших действий, точную информацию об
ошибке и т.п.). Пароли клиентов хранятся на сервере в
зашифрованном виде, поэтому нам не известен Ваш пароль.
Для решения некоторых вопросов требуется авторизация от
имени клиента. В этом случае Вы должны предоставить пароль
от аккаунта. После решения вопроса, рекомендуется сменить
пароль через Панель Управления.
