объяснения в течение суток, аккаунт блокируется. В то же
время, если вследствие нарушения Правил нормальное
функционирование сервисов Хостинга невозможно и работа
сайтов других клиентов затруднена, то аккаунт блокируется
немедленно и после этого клиенту высылается уведомление.
Также аккаунт может быть заблокирован немедленно в случае
обнаружения факта явного нарушения правил, например,
публикации на сайте порнографии или рассылки спама.
Клиент сам несет ответственность за правильность
предоставления контактного email адреса либо другой информациии,
