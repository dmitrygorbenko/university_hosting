допускается создание собственных скриптов, которые,
например, будут позволять под Вашими авторизационными
данными осуществлять доступ в панель управления хостингом
посторонним лицам и т.п. Особенные условия использования
ресурсов оговариваются в частном порядке. Клиент
самостоятельно проверяет правильность написанного кода.
В случае обнаружения "опасных" скриптов, доступ к ним
будут заблокирован до устранения ошибки. Клиенту
высылается уведомление.
</p>
